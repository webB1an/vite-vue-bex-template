const week = {
  0: '星期天',
  1: '星期一',
  2: '星期二',
  3: '星期三',
  4: '星期四',
  5: '星期五',
  6: '星期六',
}

export type Week = typeof week

export type WeekDay = keyof Week

export function getWeekday<T extends keyof Week>(day: T) {
  return week[day]
}
